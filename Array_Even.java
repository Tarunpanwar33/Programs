class Array_Even 
{
	public static void main(String[] args) 
	{
		int arr[]={1,2,47,6,22,11,67,};
		for (int i=0;i<arr.length ;i++ )
		{
			if (arr[i]%2==0)
			{
				System.out.println("the even no is :--"+arr[i]);
			}

			else{
				
				System.out.println("The odd no is :---"+arr[i]);
			}
		}
		
	}
}
